function getRandomQuestions(count = 5) {
  const shuffled = [...questions].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, Math.min(count, questions.length));
}

function getSuggestionList(question) {
  let pool;
  switch (question.type) {
    case 'club': pool = CLUBS_POOL; break;
    case 'country': pool = COUNTRIES_POOL; break;
    case 'player': default: pool = PLAYERS_POOL; break;
  }

  const correctNames = new Set();
  question.answers.forEach(answer => {
    correctNames.add(answer.name.toLowerCase());
    answer.aliases.forEach(alias => correctNames.add(alias.toLowerCase()));
  });

  const suggestions = [];

  question.answers.forEach((answer, index) => {
    suggestions.push({ name: answer.name, aliases: answer.aliases, index: index });
  });

  pool.forEach(name => {
    if (!correctNames.has(name.toLowerCase())) {
      suggestions.push({ name: name, aliases: [], index: -1 });
    }
  });

  for (let i = suggestions.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [suggestions[i], suggestions[j]] = [suggestions[j], suggestions[i]];
  }

  return suggestions;
}

